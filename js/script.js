// JavaScript to handle button click actions
function ctaAction(action) {
    switch(action) {
        case 'like':
            alert("Thank you for liking the video!");
            break;
        case 'comment':
            alert("Feel free to leave a comment!");
            break;
        case 'subscribe':
            alert("Thanks for subscribing! Stay tuned for more content.");
            break;
        case 'share':
            alert("Share this video with your friends!");
            break;
    }
}
